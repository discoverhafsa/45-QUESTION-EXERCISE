let newPlaces : string[] = ["London","saudia","Dubai","Turkey","Pakistan"];
console.log("original order",newPlaces);

//Alphabatical order
console.log("alphabatical order",newPlaces.sort());
console.log("original order",newPlaces);

//Reverse Alphabatical order
console.log("reverse alphabatic order",newPlaces.reverse());
console.log("original order",newPlaces);
//reverse order
newPlaces.reverse()
console.log("reverse",newPlaces);
newPlaces.reverse()
console.log("reverse again",newPlaces);
//sort
newPlaces.sort()
console.log("sort",newPlaces);
newPlaces.sort().reverse()
console.log("reverse sort",newPlaces);
